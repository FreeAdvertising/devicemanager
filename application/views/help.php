<h2>Help</h2>
<p>Welcome!  Device Manager is an internal application written by the Free Advertising web team to give Free staff a central location to turn to when they need to use hardware (such as laptops).  It was initially conceived by Marynika Lozowska and Ryan Priebe.</p>

<p>Technologies we used:</p>
<ul>
	<li>Twitter Bootstrap 3</li>
	<li>Codeigniter 2.1.4</li>
	<li>jQuery <span class="jq-version">2.0.3</span></li>
</ul>

